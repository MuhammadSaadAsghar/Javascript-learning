// comparison operators
// Comparison operators are used to compare two values in JavaScript and return a Boolean result (true or false).


// 1️ Equal To ==
// Checks if two values are equal (does type conversion).
5 == "5"     // true
// 2️ Strict Equal ===
// Checks value and data type.
5 === "5"   // false

// 3️ Not Equal !=
// Checks if values are not equal.
5 != 10   // true
// 4️ Strict Not Equal !==
// Checks value and type are not equal.
5 !== "5"   // true

// 5️ Greater Than >
10 > 5   // true
// 6️ Less Than <
3 < 8   // true

// 7️ Greater Than or Equal >=
10 >= 10   // true

// 8️ Less Than or Equal <=
5 <= 7   // true

